// Hello World in JavaScript
console.log("Hello, World!");

/*
  --- Syntax Comparison: JavaScript vs Python ---

  JavaScript:
  - Uses console.log() to print output
  - Statements end with semicolons (optional but recommended)
  - Curly braces {} define code blocks
  - Runs in browser and Node.js environments
  - Dynamically typed (var, let, const)

  Python:
  - Uses print() to output text
  - No semicolons; newlines end statements
  - Indentation (whitespace) defines code blocks
  - Runs via Python interpreter
  - Dynamically typed

  Key Structural Differences:
  | Feature         | JavaScript              | Python           |
  |-----------------|-------------------------|------------------|
  | Output          | console.log("text")     | print("text")    |
  | String          | "Hello" or 'Hello'      | "Hello"/'Hello'  |
  | Block delimiter | Curly braces {}         | Indentation      |
  | Semicolons      | Recommended             | Not used         |
  | Comments        | // single, /* multi *   | # single         |
*/
